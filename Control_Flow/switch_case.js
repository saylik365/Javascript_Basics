const month = "Apr"

switch (month) {
    // case 1:
    //     console.log("january")
    //     break;
    case "jan": 
        console.log("january")
        break;
    case "Feb":
        console.log("February")
        break;
    case "Mar":
        console.log("March")
        break;
    case "Apr":
        console.log("April")
        break;

    default:
        console.log("default case match")
        break;
}
//break statement   - breaks the control flow of the switch block.
//In a switch, it breaks out of the switch block. This stops the execution of more code inside the switch.
//it does not run default case if the break statement is used.
//break statement is used to terminate the switch block and to jump out of the switch statement.


